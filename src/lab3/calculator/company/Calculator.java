package lab3.calculator.company;

public class Calculator {




    public float addition(int first, float second) {
        float sum = first + second;
        return sum;
    }
    public float division(float third, int fourth) {
        float result1 = third / fourth;
        return result1;
    }

    public int op(int fifth, int sixth, int seventh) {
        int result2 = fifth + (sixth * seventh);
        return result2;
    }

    public float substract (float firstnumber, int secondnumber) {
        float substraction = firstnumber - secondnumber;
        return substraction;
    }
    public double inmultire(double thirdno, int fourthno) {
        double inm = thirdno * fourthno;
        return inm;
    }
    public float impartire(int fifth, float sixth) {
        float div = fifth / sixth;
        return div;
    }

    public long adunare(int seventh, long eigth) {
        long adun = seventh + eigth;
        return adun;
    }
}
